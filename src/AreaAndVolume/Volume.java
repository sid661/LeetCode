package AreaAndVolume;

public class Volume {
    public void areaSqure(int length, int breadth,int height){
        int volume = length*breadth*height;
        System.out.println(volume);

    }
    public void areaRectangle(int x , int y,int z){
        int vol = x*y*z;
        System.out.println(vol);
    }
}
